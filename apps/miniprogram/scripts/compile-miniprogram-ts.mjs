import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import ts from "typescript";

const root = process.cwd();
const marker = "// Generated from TypeScript. Do not edit this file directly.\n";

const sourceFiles = [
  "app.ts",
  "utils/request.ts",
  "pages/home/index.ts",
  "pages/setup/index.ts",
  "pages/weather/index.ts",
  "pages/assistant/index.ts",
  "pages/meals/index.ts",
  "pages/water/index.ts",
  "pages/todos/index.ts",
  "pages/anniversaries/index.ts",
  "pages/parcels/index.ts",
  "pages/expenses/index.ts",
  "pages/settings/index.ts"
];

for (const sourceFile of sourceFiles) {
  const sourcePath = join(root, sourceFile);
  const targetPath = join(root, sourceFile.replace(/\.ts$/, ".js"));
  const source = readFileSync(sourcePath, "utf8");
  const result = ts.transpileModule(source, {
    compilerOptions: {
      target: ts.ScriptTarget.ES2020,
      module: ts.ModuleKind.CommonJS,
      esModuleInterop: true,
      removeComments: false
    },
    fileName: sourceFile
  });

  mkdirSync(dirname(targetPath), { recursive: true });
  writeFileSync(targetPath, `${marker}${result.outputText}`, "utf8");
}

console.log(`Generated ${sourceFiles.length} Mini Program JavaScript files.`);
